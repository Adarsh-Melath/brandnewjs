// Arithmetic
let num1 = 10;
let num2 = 5;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % num2);
console.log(num1 ** num2);
console.log(num1++); // post-increment
console.log(++num1); // pre-increment
console.log(num2--);
console.log(--num2);

// Assignment
let a = 10;
console.log((a += 5)); // 15
console.log((a -= 5)); //10
console.log((a *= 5)); //50
console.log((a /= 5)); //10
console.log((a %= 5)); //0
console.log((a **= 5)); //0
